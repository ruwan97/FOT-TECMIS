    public void showAdmin()
    {
        LogUser adm=new LogUser();
        Connection con=null;
        
        try
        {
                con=MyConnection.getConnection();
                String result="SELECT * FROM admin where ad_id='"+adm.getId()+"'";
                PreparedStatement ps=con.prepareStatement(result);
                ResultSet rs=ps.executeQuery();
                
                if(rs.next())
                {
                    myAdmIdTxt.setText(rs.getString("ad_id"));
                    myAdmNameTxt.setText(rs.getString("ad_name"));
                    myAdmGenderTxt.setText(rs.getString("gender"));
                    myAdmPnoTxt.setText(rs.getString("ad_p_no"));
                }
               
                String result1="SELECT * FROM user where user_id='"+adm.getId()+"'";
                PreparedStatement ps1=con.prepareStatement(result1);
                ResultSet rs1=ps1.executeQuery();
                
                if(rs1.next())
                {
                    myAdmPwdTxt.setText(rs1.getString("user_pwd"));
                }
                
        }
        
        catch(Exception e)
        {
                javax.swing.JOptionPane.showMessageDialog(null,e);
        }
    }
