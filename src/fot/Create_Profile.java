    private void stuUploadActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_stuUploadActionPerformed
       JFileChooser lecChooser= new JFileChooser();
        lecChooser.showOpenDialog(null);
        File stu=lecChooser.getSelectedFile();
        stuFileName=stu.getAbsolutePath();
        ImageIcon imageStu= new ImageIcon(new ImageIcon(stuFileName).getImage().getScaledInstance(StudentProPic.getWidth(), StudentProPic.getHeight(),Image.SCALE_SMOOTH));
        StudentProPic.setIcon(imageStu);
        
        try
        {
            File stuPic =new File(stuFileName);
            FileInputStream Sis= new FileInputStream(stuPic);
            ByteArrayOutputStream Sos= new ByteArrayOutputStream();
            byte[] buf=new byte[1024];
            
            for(int readNum;(readNum=Sis.read(buf))!=-1;)
            {
                Sos.write(buf,0,readNum);
            }
            stuImage=Sos.toByteArray();
        }
        catch(Exception e)
        {
             JOptionPane.showMessageDialog(null,e);
        }
        
    }//GEN-LAST:event_stuUploadActionPerformed

