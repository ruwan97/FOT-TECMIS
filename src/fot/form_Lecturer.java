    private void lecSubmitActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_lecSubmitActionPerformed
           String password=lecPwdTxt.getText().trim();
        String user_name=lecTxt.getText().trim();

        if(user_name.isEmpty() && password.isEmpty())
        {
            errMsg.setText("Please enter both fields!!!");
        }
        else if(password.isEmpty())
        {
            errMsg.setText("Please enter password!!!");
        }
        else if(user_name.isEmpty())
        {
            errMsg.setText("Please enter user name!!!");
        }
        else{
            ResultSet rs=null;
            try{
                Connection con=MyConnection.getConnection();
                String sql="call lecLogin('"+user_name+"','"+password+"')";
                PreparedStatement ps=con.prepareStatement(sql);
                rs=ps.executeQuery();
                if(rs.next()==true)
                {
                    LogLec log=new LogLec();
                    log.setId(rs.getString("user_id"));

                    Lecturer inter=new Lecturer();
                    inter.setVisible(true);
                    this.setVisible(false);
                }
                else{
                    errMsg.setText("invalid user");
                }

            }
            catch(Exception e)
            {
                errMsg.setText("system error");
            }
        }  
    }//GEN-LAST:event_lecSubmitActionPerformed

