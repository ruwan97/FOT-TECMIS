    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
         String id=stuId.getText();
         String sub=subCode.getText();
         
        if(!sub.equals("") && !id.equals("")){
            try {
                Connection con=MyConnection.getConnection();
                String sql="select * from attendance where stu_id='"+id+"' AND sub_id='"+sub+"'";
                PreparedStatement ps=con.prepareStatement(sql);
                ResultSet rs=ps.executeQuery();
                
                if(!sub.equals("TCS2112"))
                {
                    eligibility();
                }
                
                 if(sub.equals("TCS2112"))
                {
                    eligibility_TCS();
                }
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null,"Please enter correct details!!!");
            }
        }
        else
        {
            JOptionPane.showMessageDialog(null,"Please both fields!!!");
        }
    }//GEN-LAST:event_jButton1ActionPerformed
    
     void eligibility()
    {
        Eligibility ele=new Eligibility();
        
        String sub=subCode.getText();
        String id=stuId.getText();
        
        float t=ele.getTheoryEligibility(id, sub);
        String countTh=String.format("%f", t);
        
        float p=ele.getPracticalEligibility(id, sub);
        String countPra=String.format("%f", p);
        
        ele.getTotalEligibility(id,sub);
        
        attCount.setText(countTh);
        praCount.setText(countPra);
        
        att.setText(ele.getEligible(ele.getTheoryEligibility(id, sub)));
        pra.setText(ele.getEligible(ele.getPracticalEligibility(id, sub)));
        
        
    }  
     
     
