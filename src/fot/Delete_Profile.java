    private void deleteTecSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteTecSearchMouseClicked
        String search=deleteTecIdTxt.getText();
        ProfileTecnical p=new ProfileTecnical();

        byte[] imageBytes = null;
        Image image;

        ResultSet rs=p.searchProfile(search);
        try {
            if(rs.next())
            {
                deleteTecDepIdTxt.setText(rs.getString("tec_dept_id"));
                deleteTecNameTxt.setText(rs.getString("tec_name"));
                deleteTecGenderTxt.setText(rs.getString("gender"));
                deleteTecPnoTxt.setText(rs.getString("tec_p_no"));
                deleteTecPwdTxt.setText(rs.getString("user_pwd"));

                imageBytes=rs.getBytes(4);
                image=getToolkit().createImage(imageBytes);
                ImageIcon icon= new ImageIcon(image);
                TecnicalProPic.setIcon(icon);

            }
            else
            {
                javax.swing.JOptionPane.showMessageDialog(null, "Data not in the database");
            }
        }
        catch (Exception e) {
            JOptionPane.showMessageDialog(null,"error....");
        }
    }//GEN-LAST:event_deleteTecSearchMouseClicked

