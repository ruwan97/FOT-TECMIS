    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        tec_att co=new tec_att();

        Object t= typeTxt.getSelectedItem();
        String type=t.toString();


        String id=stuIdTxt.getText().trim();
        String sub=subIdTxt.getText().trim();
        String hours=hoursTxt.getText().trim();
        String day=dayTxt.getText().trim();

        if(id.isEmpty() || sub.isEmpty() || day.isEmpty() ||hours.isEmpty())
        {
            javax.swing.JOptionPane.showMessageDialog(null, "Please enter all fields!!!");
        }
        else
        {
            co.setId(stuIdTxt.getText());
            co.setSub(subIdTxt.getText());
            co.setType(type);
            co.setDay(dayTxt.getText());
            co.setHours(hoursTxt.getText());

            co.createMedi();
            clear();
    }//GEN-LAST:event_jButton1ActionPerformed
    }
