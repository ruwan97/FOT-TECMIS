    private void editSearchMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editSearchMouseClicked
       
         Student stu=new Student();
        
         String id=stuId.getText();
       
        if(!id.equals(""))
        {
            try {               
            Connection con=MyConnection.getConnection();
            String sql="SELECT * FROM student WHERE stu_id='"+id+"'";
            PreparedStatement pst=con.prepareStatement(sql);
            ResultSet rs=pst.executeQuery();
            if(rs.next()){
                stuId.setText(rs.getString("stu_id"));
                Name.setText(rs.getString("stu_name"));
                Address.setText(rs.getString("stu_address"));
                Telephone_no.setText(rs.getString("stu_p_no"));
                Gender.setText(rs.getString("gender"));
                Dep_ID.setText(rs.getString("stu_dept_id"));
                Men_ID.setText(rs.getString("menter_id"));
                Bday.setText(rs.getString("stu_b_day"));
                }
            else
            {
                 Name.setText(null);
                Address.setText(null);
                Gender.setText(null);
                Dep_ID.setText(null);
                Men_ID.setText(null);
                Bday.setText(null);
                Telephone_no.setText(null);
                JOptionPane.showMessageDialog(null,"Please enter correct student id!!!");
            }
            }
             catch (Exception ex) {
              JOptionPane.showMessageDialog(null,"error");     
            } 
        }
        else
        {
             JOptionPane.showMessageDialog(null,"Please enter student id!!!");
        }
    }//GEN-LAST:event_editSearchMouseClicked

