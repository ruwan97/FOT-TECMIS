    public void showTec()
    {
        LogTO to=new LogTO();
        
        Connection con=null;
        
        try
        {
                con=MyConnection.getConnection();
                String result="SELECT * FROM tecnical_officer where tec_id='"+to.getId()+"'";
                PreparedStatement ps=con.prepareStatement(result);
                ResultSet rs=ps.executeQuery();
                
                if(rs.next())
                {
                    id.setText(rs.getString("tec_id"));
                    depId.setText(rs.getString("tec_dept_id"));
                    name.setText(rs.getString("tec_name"));
                    gender.setText(rs.getString("gender"));
                    Pno.setText(rs.getString("tec_p_no"));
                }        
        }
        
        catch(Exception e)
        {
                javax.swing.JOptionPane.showMessageDialog(null,e);
        }
    }
    
